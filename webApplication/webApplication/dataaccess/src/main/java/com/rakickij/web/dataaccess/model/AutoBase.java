package com.rakickij.web.dataaccess.model;

public class AutoBase {
	private Long moneyFund;
	private Long totalTrips;
	private Long usersCount;
	private Long driversCount;

	public Long getMoneyFund() {
		return moneyFund;
	}

	public void setMoneyFund(Long moneyFund) {
		this.moneyFund = moneyFund;
	}

	public Long getTotalTrips() {
		return totalTrips;
	}

	public void setTotalTrips(Long totalTrips) {
		this.totalTrips = totalTrips;
	}

	public Long getUsersCount() {
		return usersCount;
	}

	public void setUsersCount(Long usersCount) {
		this.usersCount = usersCount;
	}

	public Long getDriversCount() {
		return driversCount;
	}

	public void setDriversCount(Long driversCount) {
		this.driversCount = driversCount;
	}
}
